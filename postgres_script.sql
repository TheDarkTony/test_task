create table if not exists person(
	id INT generated by default as identity primary key,
	first_name VARCHAR(50) not null,
	last_name VARCHAR(50) not null,
	email VARCHAR(50) unique not null,
	email_verified BOOLEAN default false,
	email_2fa_enabled BOOLEAN default false,
	password VARCHAR(80) not null,
	is_active BOOLEAN default true,
	created_at TIMESTAMP default NOW()
);

create table if not exists workout
(
	id INT generated by default as identity primary key,
	description VARCHAR(100) not null,
	date TIMESTAMP default now(),
	activity varchar(200) not null,
	result varchar(200) null,
	sportsman_id INT not null,
	coach_id INT null,
	
	constraint fk_sportsman_person
		foreign key (sportsman_id)
		references person(id),
		
	constraint fk_coach_person
		foreign key (coach_id)
		references person(id)
);

create table if not exists role
(
	role varchar(25) primary key,
	description varchar(100) not null
);

create table person_role
(
	id INT generated by default as identity primary key,
	person_id int not null,
	role varchar(25) not null,
	
	constraint fk_person
		foreign key (person_id)
		references person(id)
);


create table if not exists permissions
(
	id INT generated by default as identity primary key,
	entity varchar(25) not null,
	role varchar(25) not null,
	allow_create boolean not null default false,
	allow_edit boolean not null default false,
	allow_read boolean not null default false,
	mode varchar(5) not null,
	person_id int null,
	
	constraint fk_permission_person
		foreign key (person_id)
		references person(id)
);





